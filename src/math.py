# Maths functions test

class Math:
	""" class of demo maths function """

	def addition(val1, val2):
		""" add 2 numbers together """
		return val1 + val2

	def subtraction(val1, val2):
		""" gets the difference between 2 numbers """
		return val1 - val2

	def multiplication(val1, val2):
		""" gets the product of 2 numbers """
		return val1 * val2

	def division(val1, val2):
		""" gets the quotient of 2 numbers """
		return val1 / val2